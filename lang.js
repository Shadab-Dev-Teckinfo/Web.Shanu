const supportedLanguages = ["en", "ar"];

function getCurrentLang() {
  const savedLang = localStorage.getItem("shanuLang");
  if (savedLang && supportedLanguages.includes(savedLang)) return savedLang;

  const browserLang = navigator.language?.slice(0, 2).toLowerCase();
  if (supportedLanguages.includes(browserLang)) return browserLang;

  return "en";
}

function translateElementText(element, dictionary) {
  const key = element.getAttribute("data-i18n");
  if (dictionary[key]) element.textContent = dictionary[key];
}

function translateElementAttribute(selector, attribute, dictionary) {
  document.querySelectorAll(selector).forEach((element) => {
    const key = element.getAttribute(selector.slice(1, -1));
    if (dictionary[key]) element.setAttribute(attribute, dictionary[key]);
  });
}

function applyLanguage(lang = getCurrentLang()) {
  const activeLang = supportedLanguages.includes(lang) ? lang : "en";
  const dictionary = translations[activeLang] || translations.en;

  document.documentElement.lang = activeLang;
  document.documentElement.dir = activeLang === "ar" ? "rtl" : "ltr";
  document.body.classList.toggle("rtl", activeLang === "ar");
  document.body.classList.toggle("ltr", activeLang !== "ar");

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    translateElementText(element, dictionary);
  });

  translateElementAttribute("[data-i18n-placeholder]", "placeholder", dictionary);
  translateElementAttribute("[data-i18n-alt]", "alt", dictionary);
  translateElementAttribute("[data-i18n-aria-label]", "aria-label", dictionary);
  translateElementAttribute("[data-i18n-title]", "title", dictionary);

  document.querySelectorAll("#languageSelector option").forEach((option) => {
    const key = option.getAttribute("data-i18n");
    if (dictionary[key]) option.textContent = dictionary[key];
    option.selected = option.value === activeLang;
  });

  document.dispatchEvent(new CustomEvent("languageChanged", { detail: { lang: activeLang, dictionary } }));
}

document.addEventListener("DOMContentLoaded", () => {
  const selector = document.getElementById("languageSelector");
  if (selector) {
    selector.addEventListener("change", (event) => {
      const selectedLang = event.target.value;
      localStorage.setItem("shanuLang", selectedLang);
      applyLanguage(selectedLang);
    });
  }

  applyLanguage();
});
